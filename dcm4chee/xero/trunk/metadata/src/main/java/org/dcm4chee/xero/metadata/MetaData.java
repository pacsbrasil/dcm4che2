package org.dcm4chee.xero.metadata;

import static java.lang.annotation.ElementType.FIELD;
import static java.lang.annotation.ElementType.METHOD;
import static java.lang.annotation.RetentionPolicy.RUNTIME;

import java.lang.annotation.Documented;
import java.lang.annotation.Retention;
import java.lang.annotation.Target;

/**
 * This annotation causes the meta-data associated with the current instance of the
 * object to be injected, based on the name of the element it is attached to.
 * @author bwallace
 *
 */
@Target({FIELD, METHOD})
@Retention(RUNTIME)
@Documented
public @interface MetaData {
   /**
    * The meta-data name to use, relative to the parent meta-data.
    */
   String value() default "";
   
   /**
    * Specifies that a component should be instantiated
    * if the context variable is null.
    */
   boolean create() default false;
   
   /**
    * Specifies that the injected value must not be
    * null, by default.
    */
   boolean required() default true;

   /**
    * Specifies the value that is to be out-jected into the meta-data.
    * @return
    */
   String out() default "";
}